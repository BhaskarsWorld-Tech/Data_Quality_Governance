);

INSERT INTO FACT_SUBSCRIPTION
SELECT
    SEQ4() + 1 AS SUBSCRIPTION_ID,
    UNIFORM(1, 5001, RANDOM()) AS CUSTOMER_ID,
    UNIFORM(1, 9, RANDOM()) AS PRODUCT_ID,
    DATEADD(DAY, UNIFORM(0, 900, RANDOM()), '2023-01-01')::DATE AS START_DATE,
    DATEADD(DAY, UNIFORM(365, 1095, RANDOM()), START_DATE)::DATE AS END_DATE,
    CASE MOD(SUBSCRIPTION_ID, 3)
        WHEN 0 THEN 'Monthly'
        WHEN 1 THEN 'Annual'
        ELSE 'Quarterly'
    END AS BILLING_FREQUENCY,
    ROUND(UNIFORM(5000, 100000, RANDOM()), 2) AS ARR_AMOUNT,
    ROUND(ARR_AMOUNT / 12, 2) AS MRR_AMOUNT,
    CASE MOD(SUBSCRIPTION_ID, 10)
        WHEN 0 THEN 'Churned'
        WHEN 1 THEN 'Paused'
        ELSE 'Active'
    END AS SUBSCRIPTION_STATUS
FROM TABLE(GENERATOR(ROWCOUNT => 15000));

CREATE OR REPLACE TABLE FACT_SALES_QUOTA (
    QUOTA_ID NUMBER,
    SALES_REP_ID NUMBER,
    FISCAL_YEAR NUMBER,
    FISCAL_QUARTER NUMBER,
    QUOTA_AMOUNT NUMBER(14,2)
);

INSERT INTO FACT_SALES_QUOTA
SELECT
    ROW_NUMBER() OVER (ORDER BY r.SALES_REP_ID, y.FISCAL_YEAR, q.FISCAL_QUARTER) AS QUOTA_ID,
    r.SALES_REP_ID,
    y.FISCAL_YEAR,
    q.FISCAL_QUARTER,
    CASE r.REGION
        WHEN 'West' THEN 350000
        WHEN 'East' THEN 325000
        WHEN 'Central' THEN 275000
        ELSE 250000
    END + UNIFORM(0, 100000, RANDOM()) AS QUOTA_AMOUNT
FROM DIM_SALES_REP r
CROSS JOIN (SELECT 2023 AS FISCAL_YEAR UNION ALL SELECT 2024 UNION ALL SELECT 2025 UNION ALL SELECT 2026) y
CROSS JOIN (SELECT 1 AS FISCAL_QUARTER UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4) q;

