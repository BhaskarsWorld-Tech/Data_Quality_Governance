CREATE OR REPLACE TABLE DIM_DATE AS
SELECT
    DATEADD(DAY, SEQ4(), '2023-01-01')::DATE AS DATE_KEY,
    YEAR(DATE_KEY) AS CALENDAR_YEAR,
    QUARTER(DATE_KEY) AS CALENDAR_QUARTER,
    MONTH(DATE_KEY) AS CALENDAR_MONTH,
    TO_CHAR(DATE_KEY, 'MMMM') AS MONTH_NAME,
    WEEK(DATE_KEY) AS WEEK_OF_YEAR,
    DAYOFWEEK(DATE_KEY) AS DAY_OF_WEEK,
    DAYNAME(DATE_KEY) AS DAY_NAME,
    CASE WHEN DAYOFWEEK(DATE_KEY) IN (1,7) THEN TRUE ELSE FALSE END AS IS_WEEKEND,
    CASE
        WHEN MONTH(DATE_KEY) >= 2 THEN YEAR(DATE_KEY)
        ELSE YEAR(DATE_KEY) - 1
    END AS FISCAL_YEAR,
    CASE
        WHEN MONTH(DATE_KEY) IN (2,3,4) THEN 1
        WHEN MONTH(DATE_KEY) IN (5,6,7) THEN 2
        WHEN MONTH(DATE_KEY) IN (8,9,10) THEN 3
        ELSE 4
    END AS FISCAL_QUARTER
FROM TABLE(GENERATOR(ROWCOUNT => 1461));

