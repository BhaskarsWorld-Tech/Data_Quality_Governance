CREATE OR REPLACE VIEW VW_DATA_QUALITY_CHECKS AS
SELECT 'FACT_SALES_ORDER' AS TABLE_NAME, 'ORDER_ID_NULL' AS CHECK_NAME, COUNT(*) AS FAILED_RECORDS
FROM FACT_SALES_ORDER WHERE ORDER_ID IS NULL
UNION ALL
SELECT 'FACT_SALES_ORDER', 'NET_AMOUNT_NEGATIVE', COUNT(*)
FROM FACT_SALES_ORDER WHERE NET_AMOUNT < 0
UNION ALL
SELECT 'FACT_SALES_ORDER', 'INVALID_ORDER_STATUS', COUNT(*)
FROM FACT_SALES_ORDER WHERE ORDER_STATUS NOT IN ('Completed', 'Cancelled', 'Returned')
UNION ALL
SELECT 'FACT_SALES_ORDER', 'INVALID_PAYMENT_STATUS', COUNT(*)
FROM FACT_SALES_ORDER WHERE PAYMENT_STATUS NOT IN ('Paid', 'Pending', 'Failed')
UNION ALL
SELECT 'DIM_CUSTOMER', 'CUSTOMER_NAME_NULL', COUNT(*)
FROM DIM_CUSTOMER WHERE CUSTOMER_NAME IS NULL
UNION ALL
SELECT 'DIM_PRODUCT', 'UNIT_PRICE_LESS_THAN_COST', COUNT(*)
FROM DIM_PRODUCT WHERE UNIT_PRICE < COST_PRICE;

