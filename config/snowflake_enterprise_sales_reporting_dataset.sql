CREATE OR REPLACE VIEW VW_SALES_DETAIL AS
SELECT
    f.ORDER_ID,
    f.ORDER_DATE,
    d.CALENDAR_YEAR,
    d.CALENDAR_QUARTER,
    d.CALENDAR_MONTH,
    d.MONTH_NAME,
    d.FISCAL_YEAR,
    d.FISCAL_QUARTER,
    c.CUSTOMER_ID,
    c.CUSTOMER_NAME,
    c.CUSTOMER_SEGMENT,
    c.INDUSTRY,
    c.COMPANY_SIZE,
    c.CITY,
    c.STATE,
    c.COUNTRY,
    c.REGION AS CUSTOMER_REGION,
    p.PRODUCT_ID,
    p.PRODUCT_NAME,
    p.PRODUCT_CATEGORY,
    p.PRODUCT_SUBCATEGORY,
    p.PRODUCT_FAMILY,
    s.SALES_REP_ID,
    s.SALES_REP_NAME,
    s.REGION AS SALES_REGION,
    s.TERRITORY,
    s.MANAGER_NAME,
    ch.CHANNEL_NAME,
    ch.CHANNEL_TYPE,
    ca.CAMPAIGN_NAME,
    ca.CAMPAIGN_TYPE,
    f.QUANTITY,
    f.UNIT_PRICE,
    f.GROSS_AMOUNT,
    f.DISCOUNT_AMOUNT,
    f.TAX_AMOUNT,
    f.NET_AMOUNT,
    f.COST_AMOUNT,
    f.PROFIT_AMOUNT,
    ROUND(f.PROFIT_AMOUNT / NULLIF(f.NET_AMOUNT, 0), 4) AS PROFIT_MARGIN,
    f.PAYMENT_STATUS,
    f.ORDER_STATUS
FROM FACT_SALES_ORDER f
LEFT JOIN DIM_DATE d ON f.ORDER_DATE = d.DATE_KEY
LEFT JOIN DIM_CUSTOMER c ON f.CUSTOMER_ID = c.CUSTOMER_ID
LEFT JOIN DIM_PRODUCT p ON f.PRODUCT_ID = p.PRODUCT_ID
LEFT JOIN DIM_SALES_REP s ON f.SALES_REP_ID = s.SALES_REP_ID
LEFT JOIN DIM_CHANNEL ch ON f.CHANNEL_ID = ch.CHANNEL_ID
LEFT JOIN DIM_CAMPAIGN ca ON f.CAMPAIGN_ID = ca.CAMPAIGN_ID;

CREATE OR REPLACE VIEW VW_EXECUTIVE_REVENUE_SUMMARY AS
SELECT
    FISCAL_YEAR,
    FISCAL_QUARTER,
    COUNT(DISTINCT ORDER_ID) AS TOTAL_ORDERS,
    COUNT(DISTINCT CUSTOMER_ID) AS ACTIVE_CUSTOMERS,
    SUM(GROSS_AMOUNT) AS GROSS_REVENUE,
    SUM(DISCOUNT_AMOUNT) AS TOTAL_DISCOUNT,
    SUM(NET_AMOUNT) AS NET_REVENUE,
    SUM(COST_AMOUNT) AS TOTAL_COST,
    SUM(PROFIT_AMOUNT) AS TOTAL_PROFIT,
    ROUND(SUM(PROFIT_AMOUNT) / NULLIF(SUM(NET_AMOUNT), 0), 4) AS PROFIT_MARGIN
FROM VW_SALES_DETAIL
WHERE ORDER_STATUS = 'Completed'
GROUP BY FISCAL_YEAR, FISCAL_QUARTER;

CREATE OR REPLACE VIEW VW_MONTHLY_REVENUE_TREND AS
