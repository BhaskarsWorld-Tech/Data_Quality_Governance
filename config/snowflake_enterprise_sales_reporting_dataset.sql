CREATE OR REPLACE TABLE FACT_SALES_ORDER (
    ORDER_ID NUMBER,
    ORDER_DATE DATE,
    CUSTOMER_ID NUMBER,
    PRODUCT_ID NUMBER,
    SALES_REP_ID NUMBER,
    CHANNEL_ID NUMBER,
    CAMPAIGN_ID NUMBER,
    QUANTITY NUMBER,
    UNIT_PRICE NUMBER(12,2),
    GROSS_AMOUNT NUMBER(14,2),
    DISCOUNT_AMOUNT NUMBER(14,2),
    TAX_AMOUNT NUMBER(14,2),
    NET_AMOUNT NUMBER(14,2),
    COST_AMOUNT NUMBER(14,2),
    PROFIT_AMOUNT NUMBER(14,2),
    PAYMENT_STATUS STRING,
    ORDER_STATUS STRING,
    CREATED_AT TIMESTAMP_NTZ
);

INSERT INTO FACT_SALES_ORDER
WITH base AS (
    SELECT
        SEQ4() + 1 AS ORDER_ID,
        DATEADD(DAY, UNIFORM(0, 1095, RANDOM()), '2023-01-01')::DATE AS ORDER_DATE,
        UNIFORM(1, 5001, RANDOM()) AS CUSTOMER_ID,
        UNIFORM(1, 13, RANDOM()) AS PRODUCT_ID,
        UNIFORM(1, 101, RANDOM()) AS SALES_REP_ID,
        UNIFORM(1, 6, RANDOM()) AS CHANNEL_ID,
        UNIFORM(1, 6, RANDOM()) AS CAMPAIGN_ID,
        UNIFORM(1, 11, RANDOM()) AS QUANTITY
    FROM TABLE(GENERATOR(ROWCOUNT => 120000))
),
priced AS (
    SELECT
        b.*,
        p.UNIT_PRICE,
        p.COST_PRICE,
        ROUND(b.QUANTITY * p.UNIT_PRICE, 2) AS GROSS_AMOUNT,
        ROUND((b.QUANTITY * p.UNIT_PRICE) * UNIFORM(0, 21, RANDOM()) / 100, 2) AS DISCOUNT_AMOUNT,
        ROUND((b.QUANTITY * p.UNIT_PRICE) * 0.08, 2) AS TAX_AMOUNT
    FROM base b
    JOIN DIM_PRODUCT p ON b.PRODUCT_ID = p.PRODUCT_ID
)
SELECT
    ORDER_ID,
    ORDER_DATE,
    CUSTOMER_ID,
    PRODUCT_ID,
    SALES_REP_ID,
    CHANNEL_ID,
    CAMPAIGN_ID,
    QUANTITY,
    UNIT_PRICE,
    GROSS_AMOUNT,
    DISCOUNT_AMOUNT,
    TAX_AMOUNT,
    ROUND(GROSS_AMOUNT - DISCOUNT_AMOUNT + TAX_AMOUNT, 2) AS NET_AMOUNT,
    ROUND(QUANTITY * COST_PRICE, 2) AS COST_AMOUNT,
    ROUND((GROSS_AMOUNT - DISCOUNT_AMOUNT + TAX_AMOUNT) - (QUANTITY * COST_PRICE), 2) AS PROFIT_AMOUNT,
    CASE MOD(ORDER_ID, 20)
        WHEN 0 THEN 'Failed'
        WHEN 1 THEN 'Pending'
        ELSE 'Paid'
    END AS PAYMENT_STATUS,
    CASE MOD(ORDER_ID, 25)
        WHEN 0 THEN 'Cancelled'
        WHEN 1 THEN 'Returned'
        ELSE 'Completed'
    END AS ORDER_STATUS,
    CURRENT_TIMESTAMP() AS CREATED_AT
FROM priced;

CREATE OR REPLACE TABLE FACT_RETURNS (
    RETURN_ID NUMBER,
    ORDER_ID NUMBER,
    RETURN_DATE DATE,
    RETURN_REASON STRING,
    RETURN_AMOUNT NUMBER(14,2),
    RETURN_STATUS STRING
);

INSERT INTO FACT_RETURNS
SELECT
    ROW_NUMBER() OVER (ORDER BY ORDER_ID) AS RETURN_ID,
    ORDER_ID,
    DATEADD(DAY, UNIFORM(1, 30, RANDOM()), ORDER_DATE)::DATE AS RETURN_DATE,
    CASE MOD(ORDER_ID, 5)
        WHEN 0 THEN 'Product Issue'
        WHEN 1 THEN 'Customer Cancelled'
        WHEN 2 THEN 'Billing Error'
        WHEN 3 THEN 'Duplicate Order'
        ELSE 'Other'
    END AS RETURN_REASON,
    ROUND(NET_AMOUNT * UNIFORM(20, 101, RANDOM()) / 100, 2) AS RETURN_AMOUNT,
    CASE WHEN MOD(ORDER_ID, 3) = 0 THEN 'Pending' ELSE 'Approved' END AS RETURN_STATUS
FROM FACT_SALES_ORDER
WHERE ORDER_STATUS = 'Returned'
   OR MOD(ORDER_ID, 40) = 0;

