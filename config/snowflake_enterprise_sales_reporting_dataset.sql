CREATE OR REPLACE VIEW VW_SALES_REP_QUOTA_ATTAINMENT AS
SELECT
    q.FISCAL_YEAR,
    q.FISCAL_QUARTER,
    s.SALES_REP_ID,
    s.SALES_REP_NAME,
    s.REGION,
    s.TERRITORY,
    s.MANAGER_NAME,
    q.QUOTA_AMOUNT,
    COALESCE(SUM(v.NET_AMOUNT), 0) AS ACTUAL_REVENUE,
    ROUND(COALESCE(SUM(v.NET_AMOUNT), 0) / NULLIF(q.QUOTA_AMOUNT, 0), 4) AS QUOTA_ATTAINMENT_RATE
FROM FACT_SALES_QUOTA q
JOIN DIM_SALES_REP s ON q.SALES_REP_ID = s.SALES_REP_ID
LEFT JOIN VW_SALES_DETAIL v
    ON q.SALES_REP_ID = v.SALES_REP_ID
   AND q.FISCAL_YEAR = v.FISCAL_YEAR
   AND q.FISCAL_QUARTER = v.FISCAL_QUARTER
   AND v.ORDER_STATUS = 'Completed'
GROUP BY
    q.FISCAL_YEAR,
    q.FISCAL_QUARTER,
    s.SALES_REP_ID,
    s.SALES_REP_NAME,
    s.REGION,
    s.TERRITORY,
    s.MANAGER_NAME,
    q.QUOTA_AMOUNT;

